// ! Data type: It specifies which type of value a variable has.​

//! Types of data types:​


//! 1. Primitive #dt​
//        - Inbuilt , independent.​
//        - Memory allocation are always stack.​
//        - Size will be fixed.​
//        - when we access the variable, we manipulate the actual value stored in that variable,  So we can say that A Primitive #dt is Accessed by Value.



//! 2. Non Primitive / Reference /Object #dt​
//        - Created by the programmer or we can say that it is user defined #dt.​
//        - Stores the reference value on the heap.
//        - when we manipulate an object, we work on the reference of that object rather than the actual object. So we can say that A Variable that stores an Object is Accessed by Reference.