//     We can also see in the console, which line is outputting or printing.

// Syntax: console.log("....");
//     it will print anything on the console.

console.log("Welcome"); 



//! Some of console Excercise 
// timer:
    console.time("code took");

// for string ​
    console.log("hello world");
    console.log('hey Welcome!');

// number ​
    console.log(45);

// array (use square brackets)​
    console.log([4,5,'hey']);

// object (curly brackets)​
    console.log({user:'this', id:5});

// table(same as object just type the table word instead of log)​
    console.table({user:'user1', id:9});

// warning​
    console.warn("hello this is the warning");

// alert ​
    console.error("Be alert!");

// time end ​
    console.timeEnd("code took");

// for clear screen ​
    console.clear();
